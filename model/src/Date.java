public class Date {
    private int day;
    private int year;
    private int month;
    private int time;


    public Date (int day,int month,int year){

    }

    public void setDay(int day) {
        this.day = day;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getDay() {
        return day;
    }

    public int getMonth() {
        return month;
    }

    public int getTime() {
        return time;
    }

    public int getYear() {
        return year;
    }
}
